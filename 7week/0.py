# Импортируем библиотеку pygame для создания графики и игр
import pygame

# Инициализируем все модули pygame (обязательно перед использованием)
pygame.init()

# ------------------ 1 часть: Настройка экрана ------------------

# Устанавливаем ширину и высоту окна
SCREEN_WIDTH = 800
SCREEN_HEIGTH = 800  # (небольшая ошибка: должно быть HEIGHT)

# Создаём окно игры с заданными размерами
screen = pygame.display.set_mode((SCREEN_WIDTH, SCREEN_HEIGTH))

# Устанавливаем заголовок окна
pygame.display.set_caption("Working with rectangles")

# Создаём объект clock, чтобы управлять скоростью обновления экрана
clock = pygame.time.Clock()

# ------------------ 2 часть: Основной игровой цикл ------------------

# Переменная run будет контролировать выполнение цикла
run = True
while run:
    
    # ------------------ 3 часть: Отрисовка и события ------------------

    # Закрашиваем весь экран в чёрный цвет (фон)
    screen.fill((0, 0, 0))  # RGB: (0, 0, 0) — чёрный цвет
    
    # Обрабатываем все события, которые произошли (например, нажатие кнопок)
    for event in pygame.event.get():
        # Если игрок нажал на крестик окна — выходим из игры
        if event.type == pygame.QUIT:
            run = False
    
    # Обновляем содержимое окна (перерисовываем экран)
    pygame.display.flip()

    # Ограничиваем количество кадров в секунду (60 FPS)
    clock.tick(60)

# Когда цикл завершён, корректно закрываем pygame
pygame.quit()
